  *miny=_miny;
  *maxx=_maxx;
  *maxy=_maxy;

  *width = (_maxx - _minx);
  *height = (_maxy - _miny);

  *_cosine = cosine;
  *_sine = sine;

  hOldSourceBitmap = SelectObject(hMemSrc, hSourceBitmap);
  SelectObject(hMemSrc, hOldSourceBitmap);
  DeleteObject(hOldSourceBitmap);
  DeleteDC(hMemSrc);
